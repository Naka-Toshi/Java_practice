public class practice3_days {
 public static void main(String args[]) {
	
	String day = "Friday";
	switch (day) {
		case "Monday":
			System.out.print("Today is " +day);
			break;
		case "Tuesday":
			System.out.print("Today is " +day);
			break;
		case "Wednesday":
			System.out.print("Today is " +day);
			break;
		case "Thursday":
			System.out.print("Today is " +day);
		case "Friday":
			System.out.print("Today is " +day);
			break;
		case "Saturday":
			System.out.print("Today is " +day);
			break;
		default:
			System.out.print("Today is " +day);
	}
 }
}